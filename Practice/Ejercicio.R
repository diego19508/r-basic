opBasic = function(a,b){
  print('Suma')
  print(a+b)
  print('Resta')
  print(paste(sprint('%i - %i',a,b)a-b))
  print(paste(sprint('%i - %i',a,b)b-a))
  print('Producto')
  print(a*b)
  print('Cociente de la division entera')
  print(a%/%b)
  print(b%/%a)
  print('Resto de la division entera')
  print(a%%b)
  print(b%%a)
}

opBasic(6,4)
